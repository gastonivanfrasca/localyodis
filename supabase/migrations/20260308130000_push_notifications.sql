create table if not exists public.push_devices (
  device_id text primary key,
  endpoint text not null unique,
  subscription jsonb not null,
  permission text not null default 'default',
  user_agent text,
  locale text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  disabled_at timestamptz
);

create table if not exists public.push_source_preferences (
  device_id text not null references public.push_devices(device_id) on delete cascade,
  source_url text not null,
  source_name text,
  enabled boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  primary key (device_id, source_url)
);

create index if not exists push_source_preferences_enabled_source_idx
  on public.push_source_preferences (source_url)
  where enabled = true;

create table if not exists public.push_source_state (
  source_url text primary key,
  last_item_key text,
  last_item_title text,
  last_item_link text,
  last_item_published_at timestamptz,
  checked_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.push_deliveries (
  id bigint generated by default as identity primary key,
  device_id text not null references public.push_devices(device_id) on delete cascade,
  source_url text not null,
  item_key text not null,
  item_title text,
  item_link text,
  payload jsonb not null default '{}'::jsonb,
  status text not null default 'pending',
  response_status integer,
  error_message text,
  created_at timestamptz not null default now(),
  sent_at timestamptz,
  unique (device_id, source_url, item_key)
);

create index if not exists push_deliveries_source_item_idx
  on public.push_deliveries (source_url, item_key);

drop trigger if exists push_devices_set_updated_at on public.push_devices;
create trigger push_devices_set_updated_at
before update on public.push_devices
for each row
execute function public.set_updated_at();

drop trigger if exists push_source_preferences_set_updated_at on public.push_source_preferences;
create trigger push_source_preferences_set_updated_at
before update on public.push_source_preferences
for each row
execute function public.set_updated_at();

drop trigger if exists push_source_state_set_updated_at on public.push_source_state;
create trigger push_source_state_set_updated_at
before update on public.push_source_state
for each row
execute function public.set_updated_at();

